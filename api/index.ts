function getGuidelineText(age: string): string {
  const data = GUIDELINES[age];
  if (!data) return "";
  let text = `【参照指針】${data.label}\n${data.note}\n\n`;
  const domains = Object.keys(data.domains).length > 0 ? data.domains : GUIDELINES["3歳児"].domains;
  for (const [domain, info] of Object.entries(domains)) {
    text += `■ ${domain}\nねらい：\n${info.goals.map((g, i) => `${i + 1}. ${g}`).join("\n")}\n内容：\n${info.content.map((c, i) => `${i + 1}. ${c}`).join("\n")}\n\n`;
  }
  if (age === "5歳児") text += `\n■ 幼児期の終わりまでに育ってほしい10の姿\n${TEN_FIGURES.map((f, i) => `${i + 1}. ${f}`).join("\n")}\n`;
  if (age === "4歳児") text += `\n■ 4歳児の発達の特徴\n${AGE4_FEATURES.map((f) => `・${f}`).join("\n")}\n`;
  return text;
}

